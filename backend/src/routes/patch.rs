#[derive(Debug, Deserialize, Default, PartialEq, Clone, Copy)]
#[serde(rename_all = "snake_case")]
enum TagMode {
    #[default]
    Merge,
    Replace,
    Skip,
}

#[derive(Debug, Deserialize, Default, PartialEq, Clone, Copy)]
#[serde(rename_all = "snake_case")]
enum ImageMode {
    /// Drop the model's rendered previews and make the scraped image its picture.
    #[default]
    ReplaceGenerated,
    /// Keep the renders; add the scraped image and make it primary.
    Add,
    Skip,
}

#[derive(Deserialize, Default)]
struct ApplyOptions {
    /// Patch model *keys* (indices, as strings) whose matched model should be
    /// renamed to the scraped name. Per-model, not all-or-nothing — a rename is a
    /// bigger commitment than a tag. Keyed by index, not name: names are not unique.
    #[serde(default)]
    rename: Vec<String>,
    #[serde(default)]
    model_tags: TagMode,
    #[serde(default)]
    model_images: ImageMode,
    /// Insert each patch model's `description_md` as a new description
    /// revision on its matched model.
    #[serde(default)]
    model_descriptions: bool,
    #[serde(default)]
    bundle_cover: bool,
    #[serde(default)]
    bundle_description: bool,
    /// Resolves ambiguous/contested rows and adopts unmatched ones: patch model
    /// key (index, as a string) → the member id to apply it to. Overrides the
    /// automatic single match too.
    #[serde(default)]
    matches: HashMap<String, Uuid>,
}

#[derive(Serialize, ToSchema, Default)]
struct ApplyResult {
    models_updated: usize,
    images_added: usize,
    tags_added: usize,
    aliases_added: usize,
    descriptions_added: usize,
    /// Custom field values written, on the bundle and its members together.
    custom_fields_set: usize,
    /// Models whose `creator_ref` ("Creator ID") the patch set or corrected.
    creator_refs_set: usize,
}

/// A blob already put into the store, ready to become an image row.
struct StagedImage {
    sha256: String,
    size: i64,
    mime: String,
}

async fn stage_image(
    state: &AppState,
    images: &HashMap<String, Vec<u8>>,
    path: &str,
) -> Result<Option<StagedImage>, ApiError> {
    let Some(bytes) = images.get(path) else {
        return Ok(None);
    };
    let mime = mime_guess::from_path(path)
        .first()
        .map(|m| m.to_string())
        .unwrap_or_else(|| "image/png".into());
    let bytes = bytes.clone();
    let stream = futures::stream::once(async move { Ok(bytes::Bytes::from(bytes)) });
    let blob = state.store.put(Box::pin(stream)).await?;
    Ok(Some(StagedImage {
        sha256: blob.sha256,
        size: blob.size,
        mime,
    }))
}

#[tracing::instrument(skip_all, fields(bundle = %bundle_id))]
async fn apply(
    State(state): State<AppState>,
    user: User,
    Path(bundle_id): Path<Uuid>,
    multipart: Multipart,
) -> Result<Json<ApplyResult>, ApiError> {
    tracing::info!("patch apply: start");
    let created_by = bundle_created_by(&state, bundle_id).await?;
    user.require_can_edit(created_by)?;

    // options come as a text field alongside the file(s); read them together.
    let upload = read_upload(multipart).await?;
    let options: ApplyOptions = match upload.options.as_deref() {
        Some(text) => serde_json::from_str(text)
            .map_err(|e| ApiError::BadRequest(format!("bad options: {e}")))?,
        None => ApplyOptions::default(),
    };
    // Same merge as the preview, so `key` means the same thing in both — which
    // is what `options.rename` and `options.matches` are indexed by. Send the
    // same files and the numbering is identical; see `read_merged`.
    let (archive, _files) = read_merged(upload.files).await?;

    let members = bundle_members(&state, bundle_id).await?;
    let resolution = resolve(&archive.patch, &members);
    tracing::info!(
        patch_models = archive.patch.models.len(),
        members = members.len(),
        "patch apply: archive read, members fetched"
    );

    // A member contested by several patch models never auto-applies — the same
    // rule preview shows — so the two "Gold" entries cannot both silently land on
    // one model. Only an explicit `matches` entry resolves a contested one.
    let mut sole_counts: HashMap<Uuid, usize> = HashMap::new();
    for ids in &resolution {
        if let [id] = ids.as_slice() {
            *sole_counts.entry(*id).or_default() += 1;
        }
    }

    // Which member each patch model applies to: the caller's explicit choice
    // (keyed by index), else the automatic single, uncontested match.
    let mut targets: Vec<(usize, Uuid)> = Vec::new();
    for (idx, ids) in resolution.iter().enumerate() {
        let chosen =
            options
                .matches
                .get(&idx.to_string())
                .copied()
                .or_else(|| match ids.as_slice() {
                    [id] if sole_counts.get(id).copied().unwrap_or(0) == 1 => Some(*id),
                    _ => None,
                });
        if let Some(model_id) = chosen {
            targets.push((idx, model_id));
        }
    }

    // Put every image blob before opening the transaction — a blob put is async
    // filesystem work, and a rollback must not have to un-write the store.
    let mut model_images: HashMap<usize, Vec<StagedImage>> = HashMap::new();
    if options.model_images != ImageMode::Skip {
        for (idx, _) in &targets {
            let mut staged_all = Vec::new();
            for path in archive.patch.models[*idx].image_paths() {
                if let Some(staged) = stage_image(&state, &archive.images, path).await? {
                    staged_all.push(staged);
                }
            }
            if !staged_all.is_empty() {
                model_images.insert(*idx, staged_all);
            }
        }
    }
    let mut cover_images: Vec<StagedImage> = Vec::new();
    if options.bundle_cover {
        for path in &archive.patch.bundle.images {
            if let Some(staged) = stage_image(&state, &archive.images, path).await? {
                cover_images.push(staged);
            }
        }
    }

    let mut result = ApplyResult {
        models_updated: 0,
        images_added: 0,
        tags_added: 0,
        aliases_added: 0,
        descriptions_added: 0,
        custom_fields_set: 0,
        creator_refs_set: 0,
    };
    let custom_fields = fields_by_key(&mut *state.db.acquire().await?).await?;
    let mut tx = state.db.begin().await?;

    // ---- bundle-level ----
    if options.bundle_description
        && let Some(body) = archive
            .patch
            .bundle
            .description_md
            .as_deref()
            .map(str::trim)
            .filter(|d| !d.is_empty())
    {
        sqlx::query!(
            "INSERT INTO bundle_description_revisions (bundle_id, body_md, created_by)
             VALUES ($1, $2, $3)",
            bundle_id,
            body,
            user.id,
        )
        .execute(&mut *tx)
        .await?;
    }
    if options.bundle_cover && !cover_images.is_empty() {
        // First cover becomes primary; the rest are added alongside.
        sqlx::query!(
            "UPDATE images SET is_primary = false WHERE bundle_id = $1 AND is_primary",
            bundle_id,
        )
        .execute(&mut *tx)
        .await?;
        for (n, img) in cover_images.iter().enumerate() {
            if insert_image(&mut tx, ImageOwner::Bundle(bundle_id), img, n == 0, user.id).await? {
                result.images_added += 1;
            }
        }
    }

    // The bundle's creator and source page ride along with the scrape. Resolve
    // them once, then fill any matched model that has none of its own — the
    // carve rarely knows either, and "if it has one already, leave it" mirrors
    // how an import stamps a member without clobbering.
    let patch_creator_id = match archive.patch.bundle.creator.as_deref() {
        Some(name) => resolve_creator(&mut tx, name).await?,
        None => None,
    };
    let patch_source_url = archive
        .patch
        .source
        .url
        .as_deref()
        .map(str::trim)
        .filter(|u| !u.is_empty());

    // The scrape *is* this bundle's page, so its creator and source are the
    // bundle's own — replace what the bundle carries (a carve rarely knows
    // either). This is `replace`, not the `fill-if-empty` used for members
    // below: a member can have a creator of its own to protect, the bundle
    // cannot — its creator is whoever the scrape says made it.
    if patch_creator_id.is_some() || patch_source_url.is_some() {
        sqlx::query!(
            r#"UPDATE bundles SET
                 creator_id = coalesce($2::uuid, creator_id),
                 source_url = coalesce($3::text, source_url)
               WHERE id = $1"#,
            bundle_id,
            patch_creator_id,
            patch_source_url,
        )
        .execute(&mut *tx)
        .await?;
    }

    // The bundle's name: the scrape carries the real title, which beats the
    // archive's filename a drop named it after — but only when nobody has
    // renamed it since. A user-chosen name is left alone. The slug follows the
    // new name, keeping its token (see bundles::unique_slug). The flag stays set:
    // a metadata import is not a user, so a better scrape may still correct it.
    if let Some(new_name) = archive
        .patch
        .bundle
        .name
        .as_deref()
        .map(str::trim)
        .filter(|n| !n.is_empty())
    {
        let row = sqlx::query!(
            r#"SELECT name_autogenerated, slug FROM bundles WHERE id = $1"#,
            bundle_id,
        )
        .fetch_one(&mut *tx)
        .await?;
        if row.name_autogenerated {
            let slug = crate::routes::bundles::unique_slug(
                &state,
                new_name,
                Some(&row.slug),
                Some(bundle_id),
            )
            .await?;
            sqlx::query!(
                "UPDATE bundles SET name = $2, slug = $3, updated_at = now() WHERE id = $1",
                bundle_id,
                new_name,
                slug,
            )
            .execute(&mut *tx)
            .await?;
        }
    }

    // ---- per model ----
    for (idx, model_id) in &targets {
        let pm = &archive.patch.models[*idx];
        // The name the model currently carries, for the alias dedup and the
        // "name left behind" a rename records.
        let current_name = members
            .iter()
            .find(|m| m.id == *model_id)
            .map(|m| m.name.as_str())
            .unwrap_or_default();
        let per_model = PerModelOptions {
            rename: options.rename.contains(&idx.to_string()),
            model_tags: options.model_tags,
            model_images: options.model_images,
            model_descriptions: options.model_descriptions,
        };
        apply_model_metadata(
            &mut tx,
            &state,
            &user,
            pm,
            *model_id,
            current_name,
            model_images.get(idx).map(Vec::as_slice).unwrap_or(&[]),
            patch_creator_id,
            patch_source_url,
            &custom_fields,
            per_model,
            &mut result,
        )
        .await?;
    }

    // The bundle's own extra metadata, and the members it reaches from there.
    if !archive.patch.bundle.custom_fields.is_empty() {
        let skipped = apply_patch_values(
            &mut tx,
            &custom_fields,
            ValueOwner::Bundle(bundle_id),
            &archive.patch.bundle.custom_fields,
            &user,
        )
        .await?;
        result.custom_fields_set += archive.patch.bundle.custom_fields.len() - skipped.len();
        persist_bundle_fields(&mut tx, bundle_id, &user).await?;
    }

    tx.commit().await?;
    tracing::info!(
        models = result.models_updated,
        images = result.images_added,
        tags = result.tags_added,
        aliases = result.aliases_added,
        descriptions = result.descriptions_added,
        custom_fields = result.custom_fields_set,
        "patch apply: committed"
    );
    Ok(Json(result))
}

// ---------------------------------------------------------------------------
// Single-model patch: the same scraper zip, but onto one model rather than a
// bundle's members. There is nothing to match — the target is fixed — so the
// only question a multi-model patch raises is *which* patch model to apply, and
// the user answers it. The bundle block contributes only creator/source
// fill-if-empty; its name/cover/description belong to a bundle, and there isn't
// one here.
// ---------------------------------------------------------------------------

#[derive(Serialize, ToSchema)]
struct ModelPatchPreview {
    /// The model's current name — the left side of the rename diff.
    model_name: String,
    /// Whether the model already carries a creator / source URL, so the UI can
    /// say whether the bundle-level values below would actually fill anything.
    model_has_creator: bool,
    model_has_source_url: bool,
    /// The creator and source URL the patch's bundle block carries — stamped onto
    /// the model only where it has none (see the apply). Shown so the fill is not
    /// a surprise.
    bundle_creator: Option<String>,
    bundle_source_url: Option<String>,
    /// One row per model the patch carries; the user picks which applies to this
    /// model (auto-selected when there is only one).
    models: Vec<ModelPatchRow>,
}

#[derive(Serialize, ToSchema)]
struct ModelPatchRow {
    /// The patch model's index — its stable identity, the same in preview and
    /// apply (`model_index`). Names are not unique, so the UI keys on this.
    key: usize,
    patch_name: String,
    /// The tags the patch carries (lowercased); the UI shows which are new once a
    /// target is known (here, always this one model).
    tags: Vec<String>,
    has_image: bool,
    has_description: bool,
    category: Option<String>,
    /// The Creator ID this row would write — it replaces what the model has.
    creator_ref: Option<String>,
    source_url: Option<String>,
    /// Custom field values this row would write onto the model, and the ones it
    /// would skip (each with why — informational, never fatal).
    custom_fields_applied: usize,
    custom_field_warnings: Vec<CustomFieldWarning>,
}

/// Read the single zip a model patch is delivered as — the first `file` part.
/// (`read_upload` already rejects an empty upload.)
async fn read_single(files: Vec<(String, Vec<u8>)>) -> Result<Archive, ApiError> {
    let (name, bytes) = files
        .into_iter()
        .next()
        .ok_or_else(|| ApiError::BadRequest("no file field in upload".into()))?;
    read_archive_from_bytes(bytes).await.map_err(|e| match e {
        ApiError::BadRequest(m) => ApiError::BadRequest(format!("{name}: {m}")),
        other => other,
    })
}

/// Survey one patch model's custom fields against this model: how many would
/// write, and which would be skipped and why. Same primitive the bundle preview
/// uses (`resolve_patch_value`), just for a single fixed owner.
fn survey_model_fields(
    fields: &HashMap<String, CustomField>,
    pm: &PatchModel,
    model_id: Uuid,
) -> (usize, Vec<CustomFieldWarning>) {
    let mut applied = 0;
    let mut warnings = Vec::new();
    let mut keys: Vec<&String> = pm.custom_fields.keys().collect();
    keys.sort();
    for key in keys {
        match resolve_patch_value(
            fields,
            ValueOwner::Model(model_id),
            key,
            &pm.custom_fields[key],
        ) {
            Resolved::Value { .. } => applied += 1,
            Resolved::Warning(reason) => warnings.push(CustomFieldWarning {
                source: pm.label(),
                key: key.clone(),
                reason,
            }),
        }
    }
    (applied, warnings)
}

async fn model_preview(
    State(state): State<AppState>,
    user: User,
    Path(model_id): Path<Uuid>,
    multipart: Multipart,
) -> Result<Json<ModelPatchPreview>, ApiError> {
    let created_by = crate::routes::models::model_created_by(&state, model_id).await?;
    user.require_can_edit(created_by)?;

    let archive = read_single(read_upload(multipart).await?.files).await?;
    let model = sqlx::query!(
        r#"SELECT name,
                  (creator_id IS NOT NULL) as "has_creator!",
                  (source_url IS NOT NULL) as "has_source_url!"
           FROM models WHERE id = $1"#,
        model_id,
    )
    .fetch_one(&state.db)
    .await?;

    let fields = fields_by_key(&mut *state.db.acquire().await?).await?;
    let has_image = |pm: &PatchModel| {
        pm.image_paths()
            .iter()
            .any(|p| archive.images.contains_key(*p))
    };
    let models = archive
        .patch
        .models
        .iter()
        .enumerate()
        .map(|(key, pm)| {
            let (custom_fields_applied, custom_field_warnings) =
                survey_model_fields(&fields, pm, model_id);
            ModelPatchRow {
                key,
                patch_name: pm.label(),
                tags: pm.tags.iter().map(|t| t.to_lowercase()).collect(),
                has_image: has_image(pm),
                has_description: pm
                    .description_md
                    .as_deref()
                    .is_some_and(|d| !d.trim().is_empty()),
                category: pm.category.clone(),
                creator_ref: pm
                    .source_ref
                    .as_deref()
                    .map(str::trim)
                    .filter(|r| !r.is_empty())
                    .map(str::to_string),
                source_url: pm
                    .source_url
                    .as_deref()
                    .map(str::trim)
                    .filter(|u| !u.is_empty())
                    .map(str::to_string),
                custom_fields_applied,
                custom_field_warnings,
            }
        })
        .collect();

    Ok(Json(ModelPatchPreview {
        model_name: model.name,
        model_has_creator: model.has_creator,
        model_has_source_url: model.has_source_url,
        bundle_creator: archive
            .patch
            .bundle
            .creator
            .as_deref()
            .map(str::trim)
            .filter(|c| !c.is_empty())
            .map(str::to_string),
        bundle_source_url: archive
            .patch
            .source
            .url
            .as_deref()
            .map(str::trim)
            .filter(|u| !u.is_empty())
            .map(str::to_string),
        models,
    }))
}

