//! Apply a scraped bundle patch onto an existing
//! bundle: match each patch model to a member model, then merge metadata under
//! the caller's chosen rules. Two steps, like every other write in here —
//! `preview` returns the match diff to confirm, `apply` writes it.
//!
//! Generic on purpose: it knows the `meshtrove.bundle-patch/1` schema, not Loot.
//! The upload is the scraper's zip — `{ patch.json, images/* }`. The format and
//! these merge rules are specified in `docs/bundle-patch.md` — keep it true.

use std::collections::HashMap;
use std::io::{Cursor, Read};

use axum::{
    Json, Router,
    extract::{DefaultBodyLimit, Multipart, Path, State},
    routing::post,
};
use serde::{Deserialize, Serialize};
use utoipa::ToSchema;
use uuid::Uuid;

use crate::error::ApiError;
use crate::extractors::User;
use crate::routes::custom_fields::{
    CustomField, Resolved, ValueOwner, apply_patch_values, fields_by_key, persist_bundle_fields,
    resolve_patch_value,
};
use crate::routes::tags::upsert_tag;
use crate::services::blobstore::BlobStore;
use crate::state::AppState;

pub fn router() -> Router<AppState> {
    Router::new()
        .route("/api/bundles/{id}/patch/preview", post(preview))
        .route("/api/bundles/{id}/patch", post(apply))
        .route("/api/models/{id}/patch/preview", post(model_preview))
        .route("/api/models/{id}/patch", post(model_apply))
        // The zip carries images; the store streams, so no body cap.
        .layer(DefaultBodyLimit::disable())
}

// ---------------------------------------------------------------------------
// The patch document — only the fields we read; everything else is ignored, so
// a richer patch from some future scraper still applies.
// ---------------------------------------------------------------------------

#[derive(Deserialize, Default)]
struct Patch {
    #[serde(default)]
    source: PatchSource,
    #[serde(default)]
    bundle: PatchBundle,
    #[serde(default)]
    models: Vec<PatchModel>,
}

/// Where the scrape came from. Only `url` interests us — the page the bundle was
/// bought/scraped from, which we stamp onto member models that have no source.
#[derive(Deserialize, Default)]
struct PatchSource {
    #[serde(default)]
    url: Option<String>,
}

#[derive(Deserialize, Default)]
struct PatchBundle {
    /// The bundle's real title from the scrape. Replaces the bundle's name only
    /// if that name was autogenerated (the archive's, never set by a user).
    #[serde(default)]
    name: Option<String>,
    /// The site/author the bundle came from, by name — resolved to a `creators`
    /// row and stamped onto member models that have no creator yet.
    #[serde(default)]
    creator: Option<String>,
    #[serde(default)]
    description_md: Option<String>,
    /// Candidate covers, primary first (relative paths into the zip).
    #[serde(default)]
    images: Vec<String>,
    /// Admin-defined extra metadata, by field key. A key the vocabulary doesn't
    /// know is a warning in the preview, never an error — a scrape carries
    /// whatever the shop page had.
    #[serde(default)]
    custom_fields: HashMap<String, serde_json::Value>,
}

#[derive(Deserialize, Default)]
struct PatchModel {
    /// The library model this entry *is*, when the scraper already knows —
    /// the strongest match signal, consulted before any name matching. Lenient
    /// on the way in: `""` (a hand-fill file's unmatched rows) reads as absent.
    #[serde(default, deserialize_with = "de_opt_uuid")]
    uuid: Option<Uuid>,
    #[serde(default, rename = "match")]
    match_hint: MatchHint,
    #[serde(default)]
    name: Option<String>,
    #[serde(default)]
    tags: Vec<String>,
    /// Model description (markdown) — becomes a new description revision on
    /// the matched model when the apply opts in.
    #[serde(default)]
    description_md: Option<String>,
    /// Relative path into the zip, or null. One image; `images` carries a whole
    /// gallery. Kept because every patch written before `images` existed uses it.
    #[serde(default)]
    image: Option<String>,
    /// A gallery for this model, primary first — the shop-page equivalent of
    /// `bundle.images`. Additive within `/1`: a patch may send `image`, `images`,
    /// or both, and a consumer that predates this field still reads `image`.
    #[serde(default)]
    images: Vec<String>,
    /// The bundle category it came from (Heroes, Busts, …) — shown to tell two
    /// same-named models apart.
    #[serde(default)]
    category: Option<String>,
    /// This model's own page on the shop. A range sold as separate products has
    /// no single URL, so where the scraper knows the per-model one it beats the
    /// bundle-level `source.url` — see the apply below.
    #[serde(default)]
    source_url: Option<String>,
    /// The creator's own id/SKU for this model — Fat Dragon's `FDG0250`,
    /// a publisher's product code. Lands in `models.creator_ref`, the same
    /// column a carve layout fills from a `creator_ref` capture group.
    #[serde(default)]
    source_ref: Option<String>,
    /// Admin-defined extra metadata for this model, by field key. Same lenient
    /// rule as the bundle's.
    #[serde(default)]
    custom_fields: HashMap<String, serde_json::Value>,
}

/// An optional uuid that tolerates `""`/whitespace as "not given" — annotated
/// scraper files keep the field on every row and blank it where unknown.
fn de_opt_uuid<'de, D: serde::Deserializer<'de>>(d: D) -> Result<Option<Uuid>, D::Error> {
    let s: Option<String> = Option::deserialize(d)?;
    match s.as_deref().map(str::trim) {
        None | Some("") => Ok(None),
        Some(v) => Uuid::parse_str(v)
            .map(Some)
            .map_err(serde::de::Error::custom),
    }
}

#[derive(Deserialize, Default)]
struct MatchHint {
    #[serde(default)]
    name: Option<String>,
    #[serde(default)]
    aliases: Vec<String>,
}

impl PatchModel {
    /// The label the UI and the options map key this model by: its rename target,
    /// else its match name.
    fn label(&self) -> String {
        self.name
            .clone()
            .or_else(|| self.match_hint.name.clone())
            .unwrap_or_default()
    }
    /// Every image this model carries, in the order they should be applied —
    /// the singular `image` first, then `images`, deduped by path. A patch that
    /// sends the same file as both (a scraper keeping `image` populated for
    /// older consumers) contributes it once, and it stays primary.
    fn image_paths(&self) -> Vec<&str> {
        let mut paths: Vec<&str> = Vec::new();
        for p in self.image.iter().chain(self.images.iter()) {
            let p = p.as_str();
            if !p.is_empty() && !paths.contains(&p) {
                paths.push(p);
            }
        }
        paths
    }
    /// Every spelling that could match a library model.
    fn match_keys(&self) -> Vec<String> {
        let mut keys: Vec<String> = Vec::new();
        for s in self
            .name
            .iter()
            .chain(self.match_hint.name.iter())
            .chain(self.match_hint.aliases.iter())
        {
            let k = normalize(s);
            if !k.is_empty() && !keys.contains(&k) {
                keys.push(k);
            }
        }
        keys
    }
    /// Each spelling as an ordered list of words, for the prefix fallback: a
    /// member whose whole name is the leading run here (`Crocodile` in `Crocodile
    /// Buried Tomb`) is a match even though the collapsed strings differ.
    fn word_spellings(&self) -> Vec<Vec<String>> {
        self.name
            .iter()
            .chain(self.match_hint.name.iter())
            .chain(self.match_hint.aliases.iter())
            .map(|s| words(s))
            .filter(|w| !w.is_empty())
            .collect()
    }
}

/// Collapse camel case, spaces, underscores and hyphens to nothing, lowercased —
/// so the scraper's "Warrior Mummy" matches the carve's "WarriorMummy" folder
/// model without either side having to guess the other's spelling.
fn normalize(s: &str) -> String {
    s.chars()
        .filter(|c| c.is_alphanumeric())
        .flat_map(|c| c.to_lowercase())
        .collect()
}

/// The same name split into its words, in order (`AdvisorMummy` → `["advisor",
/// "mummy"]`): `expand_camel_case` puts the spaces back, then we lowercase and
/// split on anything non-alphanumeric. Ordered, unlike `normalize`, so a prefix
/// match can tell that "Crocodile Buried Tomb" *starts with* "Crocodile".
fn words(s: &str) -> Vec<String> {
    crate::util::expand_camel_case(s)
        .split(|c: char| !c.is_alphanumeric())
        .filter(|w| !w.is_empty())
        .map(str::to_lowercase)
        .collect()
}

// ---------------------------------------------------------------------------
// Reading the zip out of the multipart body.
// ---------------------------------------------------------------------------

struct Archive {
    patch: Patch,
    images: HashMap<String, Vec<u8>>,
}

/// A multipart body: every `file` field, plus the `options` text field if the
/// caller sent one (apply does, preview doesn't).
struct Upload {
    options: Option<String>,
    files: Vec<(String, Vec<u8>)>,
}

async fn read_upload(mut multipart: Multipart) -> Result<Upload, ApiError> {
    let mut upload = Upload {
        options: None,
        files: Vec::new(),
    };
    while let Some(field) = multipart
        .next_field()
        .await
        .map_err(|e| ApiError::BadRequest(format!("bad multipart body: {e}")))?
    {
        match field.name() {
            Some("options") => {
                upload.options = Some(
                    field
                        .text()
                        .await
                        .map_err(|e| ApiError::BadRequest(format!("bad options field: {e}")))?,
                );
            }
            // Repeatable. One drop of six per-model patch zips is six `file`
            // parts, and they merge into a single logical patch below.
            Some("file") => {
                let name = field.file_name().unwrap_or_default().to_string();
                let bytes = field
                    .bytes()
                    .await
                    .map_err(|e| ApiError::BadRequest(format!("reading upload: {e}")))?
                    .to_vec();
                upload.files.push((name, bytes));
            }
            _ => {}
        }
    }
    if upload.files.is_empty() {
        return Err(ApiError::BadRequest("no file field in upload".into()));
    }
    Ok(upload)
}

/// One file's contribution, for the preview's benefit — a six-zip drop should
/// say which six and what each brought.
#[derive(Serialize, ToSchema)]
struct PatchFileRow {
    name: String,
    models: usize,
}

/// Read every uploaded zip and merge them into one logical patch.
///
/// The point is that per-model patches compose: `fdg-scrape` emits one zip per
/// product page, and dropping six of them on a bundle should behave exactly
/// like dropping one six-model zip. Nothing here changes what a single file
/// means — one file in gives one archive out, unchanged.
///
/// **Order is by filename, not arrival.** `key` — a patch model's index in the
/// merged `models[]` — is the identity the preview reports and the apply
/// options refer back to, so it has to be the same in both calls. Sorting means
/// that holds even if the two requests enumerate the same files in a different
/// order, which a drag-and-drop of a multi-file selection does not promise.
/// Duplicate filenames keep their arrival order relative to each other (the
/// sort is stable), so the client contract is only "send the same files".
///
/// Bundle-level fields are **first non-empty wins**, per field. In practice
/// per-model patches carry nothing but `bundle.creator` and they all agree; the
/// rule matters when one file in the drop is a deliberate bundle-cover patch
/// (ps-scrape's second button) and the rest are models — then that file
/// supplies name/description/cover whatever position it lands in.
async fn read_merged(
    files: Vec<(String, Vec<u8>)>,
) -> Result<(Archive, Vec<PatchFileRow>), ApiError> {
    let mut files = files;
    files.sort_by(|a, b| a.0.cmp(&b.0));

    let mut merged = Archive {
        patch: Patch::default(),
        images: HashMap::new(),
    };
    let mut rows = Vec::new();
    let mut seen_cover_paths: Vec<String> = Vec::new();

    for (i, (name, bytes)) in files.into_iter().enumerate() {
        let archive = read_archive_from_bytes(bytes)
            .await
            // Which of six zips was the bad one is the only thing you want to
            // know here, and the inner error never says.
            .map_err(|e| match e {
                ApiError::BadRequest(m) => ApiError::BadRequest(format!("{name}: {m}")),
                other => other,
            })?;
        let Archive { mut patch, images } = archive;

        // Namespace every image path by the file's position, and rewrite this
        // patch's references to match, before anything is concatenated. Two
        // scrapers independently emitting `images/00.jpg` is not a collision
        // worth losing an image to — and it is the default case, since entry
        // names are per-zip and nothing coordinates them.
        //
        // Prefixing with an index that is unique per archive cannot itself
        // collide: two rewritten paths differ in their first segment unless
        // they came from the same file, where they were already distinct.
        let qualify = |path: &str| format!("{i}/{path}");
        for path in patch.bundle.images.iter_mut() {
            *path = qualify(path);
        }
        for m in patch.models.iter_mut() {
            if let Some(p) = m.image.as_mut() {
                *p = qualify(p);
            }
            for p in m.images.iter_mut() {
                *p = qualify(p);
            }
        }
        for (path, bytes) in images {
            merged.images.insert(qualify(&path), bytes);
        }

        rows.push(PatchFileRow {
            name,
            models: patch.models.len(),
        });

        // First non-empty wins, field by field.
        let b = &mut merged.patch.bundle;
        if b.name.is_none() {
            b.name = patch.bundle.name.take();
        }
        if b.creator.is_none() {
            b.creator = patch.bundle.creator.take();
        }
        if b.description_md.is_none() {
            b.description_md = patch.bundle.description_md.take();
        }
        if merged.patch.source.url.is_none() {
            merged.patch.source.url = patch.source.url.take();
        }
        for (key, value) in patch.bundle.custom_fields {
            merged
                .patch
                .bundle
                .custom_fields
                .entry(key)
                .or_insert(value);
        }
        // Covers concatenate rather than first-wins: `bundle.images` is
        // documented as "all added, the first becomes primary", so appending
        // keeps that meaning across files. Deduped by path so re-dropping the
        // same file twice doesn't queue the same cover twice.
        for path in patch.bundle.images {
            if !seen_cover_paths.contains(&path) {
                seen_cover_paths.push(path.clone());
                merged.patch.bundle.images.push(path);
            }
        }

        merged.patch.models.extend(patch.models);
    }

    Ok((merged, rows))
}

// ---------------------------------------------------------------------------
// Matching: patch models against the bundle's members.
// ---------------------------------------------------------------------------

struct Member {
    id: Uuid,
    name: String,
    tags: Vec<String>,
    /// Other names this model already answers to (from earlier imports/renames);
    /// extra lookup keys so a scrape that uses one of them still resolves.
    aliases: Vec<String>,
}

async fn bundle_members(state: &AppState, bundle_id: Uuid) -> Result<Vec<Member>, ApiError> {
    let rows = sqlx::query!(
        r#"SELECT m.id, m.name,
                  coalesce((SELECT array_agg(t.name::text ORDER BY t.name) FROM model_tags mt
                            JOIN tags t ON t.id = mt.tag_id WHERE mt.model_id = m.id), '{}')
                      as "tags!: Vec<String>",
                  coalesce((SELECT array_agg(a.alias::text ORDER BY a.alias)
                            FROM model_aliases a WHERE a.model_id = m.id), '{}')
                      as "aliases!: Vec<String>"
           FROM models m JOIN bundle_models bm ON bm.model_id = m.id
           WHERE bm.bundle_id = $1
           ORDER BY m.name"#,
        bundle_id,
    )
    .fetch_all(&state.db)
    .await?;
    Ok(rows
        .into_iter()
        .map(|r| Member {
            id: r.id,
            name: r.name,
            tags: r.tags,
            aliases: r.aliases,
        })
        .collect())
}

/// For each patch model, the member ids it could be. 0 = unmatched, 1 = matched,
/// >1 = ambiguous (the user picks).
///
/// Two tiers. First an exact match on the collapsed name, so a clean
/// "Advisor Mummy" always wins its `AdvisorMummy` folder outright. Only when that
/// finds nothing do we fall back to a prefix match: the scraped name usually
/// leads with the model and trails the bundle or category it came from
/// ("Crocodile Buried Tomb"), so a member whose whole name is the leading run of
/// words is a candidate. If several members prefix-match we keep the longest
/// (most specific) one, and if still tied use the scraped `category` — carried as
/// a tag on the model it belongs to — to break it.
///
/// Both tiers consider each member's stored aliases alongside its name, so a
/// model that a past import taught an alternate name still resolves by it.
fn resolve(patch: &Patch, members: &[Member]) -> Vec<Vec<Uuid>> {
    // Every name a member answers to, as a word list, for the prefix tier.
    let member_spellings: Vec<Vec<Vec<String>>> = members
        .iter()
        .map(|m| {
            std::iter::once(&m.name)
                .chain(m.aliases.iter())
                .map(|s| words(s))
                .filter(|w| !w.is_empty())
                .collect()
        })
        .collect();

    let mut by_key: HashMap<String, Vec<Uuid>> = HashMap::new();
    for m in members {
        for name in std::iter::once(&m.name).chain(m.aliases.iter()) {
            let key = normalize(name);
            if key.is_empty() {
                continue;
            }
            let ids = by_key.entry(key).or_default();
            if !ids.contains(&m.id) {
                ids.push(m.id);
            }
        }
    }

    patch
        .models
        .iter()
        .map(|pm| {
            // Tier 0: an explicit uuid is the patch pointing at the member
            // itself — it decides alone, names never overrule it. A uuid that
            // is not a member of this bundle matches *nothing* rather than
            // falling back to name guessing: the id says the scraper meant one
            // specific model, and that model isn't here.
            if let Some(id) = pm.uuid {
                return if members.iter().any(|m| m.id == id) {
                    vec![id]
                } else {
                    Vec::new()
                };
            }

            // Tier 1: exact on the collapsed name (or any alias).
            let mut ids: Vec<Uuid> = Vec::new();
            for key in pm.match_keys() {
                for id in by_key.get(&key).into_iter().flatten() {
                    if !ids.contains(id) {
                        ids.push(*id);
                    }
                }
            }
            if !ids.is_empty() {
                return ids;
            }

            // Tier 2: prefix fallback. A member matches if any of its spellings is
            // the leading run of a scraped spelling; its "length" for the
            // most-specific test is that of the longest such spelling.
            let scraped = pm.word_spellings();
            let matched_len = |i: usize| -> usize {
                member_spellings[i]
                    .iter()
                    .filter(|w| scraped.iter().any(|s| s.starts_with(w)))
                    .map(|w| w.len())
                    .max()
                    .unwrap_or(0)
            };
            let mut cands: Vec<usize> =
                (0..members.len()).filter(|&i| matched_len(i) > 0).collect();

            // Prefer the most specific: the longest name that still fits, so
            // "WarriorMummy" beats a bare "Warrior".
            if cands.len() > 1 {
                let longest = cands.iter().map(|&i| matched_len(i)).max().unwrap_or(0);
                cands.retain(|&i| matched_len(i) == longest);
            }

            // Still tied: the scraped category (Enemies, NPCs, …) rides along as a
            // tag on the model it belongs to. Narrow to members carrying it — but
            // only if that leaves at least one, so a category no member happens to
            // tag doesn't wipe out an otherwise-good set.
            if cands.len() > 1
                && let Some(cat) = pm
                    .category
                    .as_deref()
                    .map(str::trim)
                    .filter(|c| !c.is_empty())
            {
                let by_cat: Vec<usize> = cands
                    .iter()
                    .copied()
                    .filter(|&i| members[i].tags.iter().any(|t| t.eq_ignore_ascii_case(cat)))
                    .collect();
                if !by_cat.is_empty() {
                    cands = by_cat;
                }
            }

            cands.into_iter().map(|i| members[i].id).collect()
        })
        .collect()
}

